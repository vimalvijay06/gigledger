      "# GigLedger Phase 3 - Statistical Anomaly Detection\n",
      "\n",
      "## Why This Approach?\n",
      "\n",
      "Gig workers earn different Rs/km depending on time of day. Peak hours (12-2pm, 7-10pm) carry\n",
      "surge pricing. Comparing peak rates to off-peak rates would generate constant false alarms.\n",
      "\n",
      "**Solution: personal rolling baseline per time-of-day bucket.**\n",
      "\n",
      "1. Bucket tasks into `peak` vs `off_peak`\n",
      "2. Compute rolling 30-day baseline within each bucket (worker's own personal normal)\n",
      "3. Flag weeks where observed rate > 1.5 SD below that baseline\n",
      "4. Severity: low (1.5-2 SD), medium (2-2.5 SD), high (2.5+ SD or sustained >= 2 weeks)\n",
      "\n",
      "## Limitations\n",
      "\n",
      "| Limitation | Details |\n",
      "|---|---|\n",
      "| Minimum data guard | Need 30+ tasks per bucket or baseline SD is unreliable - no flag raised |\n",
      "| Platform policy changes | Legitimate pricing changes look like anomalies initially |\n",
      "| Distance dependency | Tasks with no distance_km are excluded from rate computation |\n",
      "| Weekly granularity | A single bad day inside an otherwise fine week may be missed |\n"
